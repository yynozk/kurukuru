  def self.create
    MapTile.init(Image[:tileset], TILE_COLUMNS, TILE_COUNT / TILE_COLUMNS)

    tiles = []
    player = nil
    MAP_CSV.each_with_index do |v, i|
      next if v == 0
      x, y = i % MAP_WIDTH, (i / MAP_WIDTH).floor
      tile_index = v - 1
      if tile_index == 0
        player = Player.new(x * TILE_WIDTH + TILE_WIDTH/2, y * TILE_HEIGHT + TILE_HEIGHT/2)
        next
      end
      tiles << MapTile.new(x, y, tile_index)
    end

    return player, self.new(tiles)
  end


  attr_reader :tiles

  def initialize(tiles)
    @tiles = tiles
  end

  def offset=(offset)
    @tiles.map.each {|t| t.offset = offset }
  end
